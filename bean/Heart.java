package com.exam.closet_f.bean;

public class Heart {
    private int outfitPic;
    private int headPic;
    private String userName;
    private String heartNum;
    private String detail;

    public String getDetail() {
        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }



    public int getOutfitPic() {
        return outfitPic;
    }

    public void setOutfitPic(int outfitPic) {
        this.outfitPic = outfitPic;
    }

    public int getHeadPic() {
        return headPic;
    }

    public void setHeadPic(int headPic) {
        this.headPic = headPic;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getHeartNum() {
        return heartNum;
    }

    public void setHeartNum(String heartNum) {
        this.heartNum = heartNum;
    }



}
